const str = "Prince Singh Chouhan";
console.log(str);

// returns boolean value if given string ends with given string or char
console.log(str.endsWith("n"))
console.log(str.endsWith("Chouhan"))
console.log(str.endsWith("prince"))

// can also take 2 parameters one for string and second for index
console.log(str.endsWith("Chouha"));
console.log(str.endsWith("Chouha", 19));